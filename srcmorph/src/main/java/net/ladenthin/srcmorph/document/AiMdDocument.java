package net.ladenthin.srcmorph.document;

import java.util.Objects;
import lombok.EqualsAndHashCode;
import lombok.ToString;
import net.ladenthin.srcmorph.support.ConvertToRecord;

/**
 * Immutable representation of an {@code .ai.md} document consisting of a header and a body.
 *
 * <p>Record-shaped value type marked {@link net.ladenthin.srcmorph.support.ConvertToRecord} for the future Java&nbsp;17+
 * migration. Accessors are record-style ({@code header()}, {@code body()}). {@code equals},
 * {@code hashCode} and {@code toString} are generated by Lombok over both fields.</p>
 */
@ConvertToRecord
@ToString
@EqualsAndHashCode
public final class AiMdDocument {
    private final AiMdHeader header;
    private final String body;

    /**
     * Creates a new {@link AiMdDocument}.
     *
     * @param header metadata header
     * @param body   markdown body text
     */
    public AiMdDocument(AiMdHeader header, String body) {
        Objects.requireNonNull(header, "header");
        Objects.requireNonNull(body, "body");
        this.header = header;
        this.body = body;
    }

    /**
     * Returns the metadata header.
     *
     * @return metadata header
     */
    public AiMdHeader header() {
        return header;
    }

    /**
     * Returns the markdown body.
     *
     * @return markdown body
     */
    public String body() {
        return body;
    }
}
